class InMemoryAccountRepository(AccountRepository):
    """In-memory account store for unit testing."""

    def __init__(self, accounts: Optional[dict[str, Account]] = None):
        self._accounts: dict[str, Account] = accounts or {}

    def _key(self, sortcode: str, acc_no: str) -> str:
        return f"{sortcode}:{acc_no}"

    def add(self, account: Account) -> None:
        self._accounts[self._key(account.sortcode, account.acc_no)] = account

    def find(self, sortcode: str, acc_no: str) -> Optional[Account]:
        return self._accounts.get(self._key(sortcode, acc_no))

    def update(self, account: Account) -> bool:
        key = self._key(account.sortcode, account.acc_no)
        if key in self._accounts:
            self._accounts[key] = account
            return True
        return False


