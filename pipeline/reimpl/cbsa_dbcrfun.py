# -- Business Logic -----------------------------------------------------------


def _is_restricted_account_type(acc_type: str) -> bool:
    """Check if the account type is MORTGAGE or LOAN.

    COBOL uses PIC X(8) so 'LOAN' is stored as 'LOAN    ' (padded).
    We strip for comparison.  The COBOL does:
        IF HV-ACCOUNT-ACC-TYPE = 'MORTGAGE'
        IF HV-ACCOUNT-ACC-TYPE = 'LOAN    '
    """
    return acc_type.strip().upper() in RESTRICTED_ACCOUNT_TYPES


def _classify_transaction(amount: Decimal, facil_type: int) -> tuple[str, str]:
    """Determine transaction type code and default description.

    From WRITE-TO-PROCTRAN-DB2 section (lines 491-517):

      IF COMM-AMT < 0                         (debit)
         MOVE 'DEB' TO HV-PROCTRAN-TYPE
         MOVE 'COUNTER WTHDRW' TO HV-PROCTRAN-DESC
         IF COMM-FACILTYPE = 496              (payment)
            MOVE 'PDR' TO HV-PROCTRAN-TYPE
            MOVE COMM-ORIGIN(1:14) TO HV-PROCTRAN-DESC
      ELSE                                    (credit)
         MOVE 'CRE' TO HV-PROCTRAN-TYPE
         MOVE 'COUNTER RECVED' TO HV-PROCTRAN-DESC
         IF COMM-FACILTYPE = 496              (payment)
            MOVE 'PCR' TO HV-PROCTRAN-TYPE
            MOVE COMM-ORIGIN(1:14) TO HV-PROCTRAN-DESC

    Returns (tran_type, default_desc).  For payment types the caller
    must override desc with origin[:14].
    """
    if amount < 0:
        if facil_type == FACILTYPE_PAYMENT:
            return "PDR", ""
        return "DEB", "COUNTER WTHDRW"
    else:
        if facil_type == FACILTYPE_PAYMENT:
            return "PCR", ""
        return "CRE", "COUNTER RECVED"


def process_debit_credit(
    request: DebitCreditRequest,
    account_repo: AccountRepository,
    proctran_repo: ProcTranRepository,
) -> DebitCreditResult:
    """Process a debit or credit transaction.

    This is the reimplementation of DBCRFUN's PROCEDURE DIVISION.
    The COBOL flow in UPDATE-ACCOUNT-DB2 section:

    1. Initialize: COMM-SUCCESS = 'N', COMM-FAIL-CODE = '0'
    2. Set COMM-SORTC from SORTCODE constant (987654)
    3. SELECT account by sortcode + acc_no
       - SQLCODE = +100 (not found)  -> fail_code '1'
       - SQLCODE != 0 (other error)  -> fail_code '2'
    4. IF COMM-AMT < 0  (debit / payment):
       a. IF acc_type is MORTGAGE or LOAN AND FACILTYPE=496
             -> fail_code '4'  (cannot debit restricted account via payment)
       b. IF avail_bal + amount < 0 AND FACILTYPE=496
             -> fail_code '3'  (insufficient funds for payment)
    5. After the debit IF block, a SECOND check (lines 368-376):
       IF acc_type is MORTGAGE or LOAN AND FACILTYPE=496
          -> fail_code '4'  (catches credits to restricted accounts via payment)
    6. Compute new balances: avail += amount, actual += amount
    7. UPDATE account in DB2
       - SQLCODE != 0 -> fail_code '2'
    8. WRITE-TO-PROCTRAN
       - INSERT fails  -> ROLLBACK account, fail_code '2'
       - INSERT succeeds -> COMM-SUCCESS='Y', COMM-FAIL-CODE='0'
    9. Return updated balances in COMMAREA
    """
    result = DebitCreditResult()
    result.sortcode = SORTCODE

    # Step 3: Read the account record
    account = account_repo.find(SORTCODE, request.acc_no)

    if account is None:
        # SQLCODE = +100 -> account not found
        result.success = False
        result.fail_code = "1"
        return result

    # Step 4: Debit-specific checks (COMM-AMT < 0)
    if request.amount < 0:
        # 4a: Restricted account type check for PAYMENT
        if _is_restricted_account_type(account.acc_type) and \
           request.facil_type == FACILTYPE_PAYMENT:
            result.success = False
            result.fail_code = "4"
            return result

        # 4b: Insufficient funds check for PAYMENT
        difference = account.avail_bal + request.amount
        if difference < 0 and request.facil_type == FACILTYPE_PAYMENT:
            result.success = False
            result.fail_code = "3"
            return result

    # Step 5: Credit check for restricted account types via PAYMENT
    # This is the second IF block in the COBOL (lines 368-376), OUTSIDE
    # the "IF COMM-AMT < 0" block.  It catches the case where a credit
    # is attempted on a MORTGAGE or LOAN account via PAYMENT.
    if _is_restricted_account_type(account.acc_type) and \
       request.facil_type == FACILTYPE_PAYMENT:
        result.success = False
        result.fail_code = "4"
        return result

    # Step 6: Compute new balances
    account.avail_bal = account.avail_bal + request.amount
    account.actual_bal = account.actual_bal + request.amount

    # Step 7: Update account in DB2
    update_ok = account_repo.update(account)

    # The COBOL moves the new balances to COMMAREA before checking SQLCODE
    result.avail_bal = account.avail_bal
    result.actual_bal = account.actual_bal

    if not update_ok:
        result.success = False
        result.fail_code = "2"
        return result

    # Step 8: Write PROCTRAN record
    tran_type, desc = _classify_transaction(request.amount, request.facil_type)

    # For PAYMENT types (PDR/PCR), description is COMM-ORIGIN(1:14)
    if request.facil_type == FACILTYPE_PAYMENT:
        desc = (request.origin or "")[:14]

    now = datetime.now()

    proctran = ProcTranRecord(
        eyecatcher="PRTR",
        sortcode=SORTCODE,
        acc_no=request.acc_no,
        date=now.strftime("%d.%m.%Y"),
        time=now.strftime("%H%M%S"),
        ref="000000000000",
        tran_type=tran_type,
        desc=desc,
        amount=request.amount,
    )

    insert_ok = proctran_repo.insert(proctran)

    if not insert_ok:
        # COBOL does SYNCPOINT ROLLBACK -- revert the account balances
        account.avail_bal = account.avail_bal - request.amount
        account.actual_bal = account.actual_bal - request.amount
        account_repo.update(account)

        result.success = False
        result.fail_code = "2"
        return result

    # Step 9: Success
    result.success = True
    result.fail_code = "0"
    return result
